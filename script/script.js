// ======================
// TDS
// ======================

function calculateTDS(interest){
    let threshold = (senior==="yes") ? 100000 : 50000;

    if(interest < threshold){
        return {applicable:false, tds:0, net:interest};
    }

    let rate = (panStatus==="no") ? 0.20 : 0.10;
    let tds = interest * rate;

    return {
        applicable:true,
        tds:tds,
        net:interest - tds,
        rate:rate*100
    };
}

