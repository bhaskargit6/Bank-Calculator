function exportPDF(){

    if(!shareData){
        alert("Please calculate first");
        return;
    }

    const rows = shareData
        .split("\n")
        .filter(l => l.trim())
        .map(line => {
            let parts = line.replace("•","").split(":");
            return [
                { text: parts[0]?.trim(), style:'label' },
                { text: parts.slice(1).join(":").trim(), style:'value' }
            ];
        });

    const docDefinition = {

        pageMargins: [25, 25, 25, 25],

        content: [

            // HEADER
            {
                text: "Deposit Summary",
                style: "header"
            },

            // CARD (NO HACKS)
            {
                margin: [0,10],
                table: {
                    widths: ['*','auto'],
                    body: rows
                },
                layout: {
                    fillColor: () => '#FFFFFF',
                    hLineColor: () => '#E0E0E0',
                    vLineWidth: () => 0,
                    paddingTop: () => 8,
                    paddingBottom: () => 8,
                    paddingLeft: () => 10,
                    paddingRight: () => 10
                }
            },

            // FOOTER
            {
                text: "Generated by Calculator",
                alignment: "center",
                style: "footer"
            }

        ],

        styles: {

            header: {
                fontSize: 20,
                bold: true,
                color: '#6750A4',
                margin: [0,0,0,10]
            },

            label: {
                fontSize: 15,
                color: '#444'
            },

            value: {
                fontSize: 15,
                bold: true,
                alignment: 'right'
            },

            footer: {
                fontSize: 10,
                color: 'gray',
                margin: [0,20,0,0]
            }

        }
    };

    pdfMake.createPdf(docDefinition).download("RIDC_Result.pdf");
}


